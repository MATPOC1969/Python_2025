"""Задача 2: Работа со списками

Создайте пустой список и с помощью функций изученных на уроке сначала добавьте в список 3 элемента(числа),
после этого вставьте 2 элемента(числа) в самое начало списка и отсортируйте список по убыванию"""

import random

def main():
    # Создаём пустой список
    numbers = []

    # Запрашиваем у пользователя границы диапазона
    min_val = int(input("Введите минимальное значение диапазона: "))
    max_val = int(input("Введите максимальное значение диапазона: "))

    # Запрашиваем у пользователя, сколько случайных чисел нужно
    count = int(input("Сколько случайных чисел нужно создать? "))

    # Заполняем список случайными числами из заданного диапазона
    for _ in range(count):
        rand_num = random.randint(min_val, max_val)
        numbers.append(rand_num)

    # Выводим получившийся список
    print("\nСгенерированный список:", numbers)

    # Суммируем элементы списка с помощью цикла for
    total_sum = 0
    for num in numbers:
        total_sum += num

    # Выводим результат сложения
    print("Сумма элементов списка =", total_sum)

    # Организуем цикл, где пользователь выбирает действие
    while True:
        print("\nЧто вы хотите сделать?")
        print("1 - Добавить число в начало списка")
        print("2 - Добавить число в конец списка")
        print("3 - Продолжить расчет (вывести результат)")

        choice = input("Введите номер пункта: ").strip()

        if choice == '1':
            # Добавить число в начало
            val = float(input("Введите число, которое хотите добавить в начало: "))
            numbers.insert(0, val)  # вставляем в начало списка
            print("Обновлённый список:", numbers)

        elif choice == '2':
            # Добавить число в конец
            val = float(input("Введите число, которое хотите добавить в конец: "))
            numbers.append(val)
            print("Обновлённый список:", numbers)

        elif choice == '3':
            # Заканчиваем добавление и переходим к дальнейшим действиям
            break
        else:
            print("Некорректный ввод. Повторите попытку.")

    # Выводим итоговый список после всех добавлений
    print("\nИтоговый список:", numbers)

    # Сортируем по возрастанию (копию), выводим
    ascending = sorted(numbers)
    print("В порядке возрастания:", ascending)

    # Сортируем по убыванию (копию), выводим
    descending = sorted(numbers, reverse=True)
    print("В порядке убывания:", descending)

    # Суммируем элементы окончательного списка
    final_sum = 0
    for num in numbers:
        final_sum += num
    print("Сумма элементов итогового списка =", final_sum)


if __name__ == "__main__":
    main()
