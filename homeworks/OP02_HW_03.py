"""Задача 3: Объединение множеств
Создайте два множества, объедините их и выведите результат

Задача 4: Пересечение множеств
Создайте два множества, найдите пересечение и выведите результат

Задача 5: Наполнение списка
Напишите программу которая будет с помощью цикла for 10 раз запрашивать у пользователя ввод
символов/цифр и добавлять эти символы/цифры в список"""

import random

def main():
    # Создание первого множества
    print("Создаём первое множество")
    min_val1 = int(input("Введите минимальное значение диапазона для первого множества: "))
    max_val1 = int(input("Введите максимальное значение диапазона для первого множества: "))
    count1 = int(input("Сколько случайных чисел нужно в первом множестве? "))

    set1 = set()
    # Генерируем случайные числа до тех пор, пока не получим нужное количество уникальных элементов
    while len(set1) < count1:
        rand_num = random.randint(min_val1, max_val1)
        set1.add(rand_num)

    # Создание второго множества
    print("\nСоздаём второе множество")
    min_val2 = int(input("Введите минимальное значение диапазона для второго множества: "))
    max_val2 = int(input("Введите максимальное значение диапазона для второго множества: "))
    count2 = int(input("Сколько случайных чисел нужно во втором множестве? "))

    set2 = set()
    while len(set2) < count2:
        rand_num = random.randint(min_val2, max_val2)
        set2.add(rand_num)

    # Выводим полученные множества
    print("\nПервое множество:", set1)
    print("Второе множество:", set2)

    # Пересечение множеств
    intersection_set = set1.intersection(set2)
    print("\nПересечение множеств:", intersection_set)

    # Объединение множеств
    union_set = set1.union(set2)
    print("Объединение множеств:", union_set)


if __name__ == "__main__":
    main()
