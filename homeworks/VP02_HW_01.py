# Запрашиваем у пользователя границы диапазона
start = int(input("Введите начало диапазона: "))
end = int(input("Введите конец диапазона: "))

# Запрашиваем у пользователя нужную цифру
digit = input("Введите цифру, которую нужно искать: ")

# Счётчик вхождений цифры во все числа диапазона
count = 0

# Перебираем каждый номер из заданного диапазона
for num in range(start, end + 1):
    # Преобразуем число в строку и считаем,
    # сколько раз указанная цифра встречается в нём
    count += str(num).count(digit)

# Выводим результат
print(f"Цифра '{digit}' встречается в заданном диапазоне {count} раз(а).")
